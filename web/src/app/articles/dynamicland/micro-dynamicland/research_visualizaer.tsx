// What’s in a Name?
// Almost everything developed at Xerox PARC had a special name. The Alto, of course, was named for Palo Alto, but the computers that succeeded it were a strange collection beginning with “D”—Dolphin, Dorado, Dandelion, Dragon, and Dandetiger (an upgrade of the Dandelion). The unofficial name of the recently announced Xerox 1185 workstation, Xerox’s new artificial-intelligence programming machine, was the Daisy.

// All of these personal computers—with their distinctive large bit-mapped screens, three-button mice, and keyboards with several unlabeled keys—are referred to as D-machines. The version of Interlisp, an artificial-intelligence language, that runs on the D-machines is known as Interlisp-D, to distinguish it from Interlisp-10, which runs on PDP-10 mainframes.

// EARS—the name of the first laser printer—stood for Ethernet-Alto-Research character generator-Scanning laser output terminal. Dover, Pimlico, Puffin, and Penguin are the names of its early successors: Dover was the first “production” laser printer, of which several dozen were made with depreciated Xerox 7000s bought from the copier division; Pimlico and Puffin were the first two color laser printers; and Penguin was a high-resolution black-and-white printer that replaced the Dover.

// The distributed-computing image at PARC, in which a network was viewed as a collection of independent agents and servers acting in concert, also led to imaginative names for individual machines. In 1983, the Knowledge Systems Area in PARC, for example, had Dolphins named Galileo, Darwin, Da Vinci, and Archimedes; Dorados named Ahwahnee, Plaza, and Waldorf; and an Alto, acting as a file server, named Ivy.

// The names were not merely clever but were also functional; for example, a user could “chat” with Ivy to learn statistics on disk space, the status of a particular file, or other information.

// “If you wanted something to last forever, you called it interim.”
// —Jim Mitchell

// In software probably the simplest progression is Smalltalk-71, Smalltalk-72, Smalltalk-76 (completed in 1977), Smalltalk-78 (completed in 1979), and Smalltalk-80(completed in 1982). In the Computer Science Laboratory, SPL (systems programming language) from the Berkeley Computer Corp. begat MPL (modular programming language), which begat Mesa, which begat Mesa-Cedar, a complete programming environment for Mesa running on D-machines. (Mesa-Cedar was later renamed Cedar to avoid confusion.)

// Giving software botanical names like Cedar (after items in the Sunset Garden Book) was a popular practice; Laurel was one of the first electronic-mail programs at PARC, and Juniper was an early (though unsuccessful) distributed file system. The file system in use today with some PARC hardware is IFS—the interim file system.

// “It got to be a joke,” said Jim Mitchell, now vice president of research at the Acorn Research Center in Palo Alto, Calif. “If you wanted something to last forever, you called it interim.”



